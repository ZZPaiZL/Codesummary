package com.vlog.pojo;

import javax.persistence.*;

@Table(name = "vlog_classify")
public class VlogClassify {
    @Id
    private String id;

    private String title;

    /**
     * @return id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return title
     */
    public String getTitle() {
        return title;
    }

    /**
     * @param title
     */
    public void setTitle(String title) {
        this.title = title;
    }
}